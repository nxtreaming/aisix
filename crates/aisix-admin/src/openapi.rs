/// `allOf` wrapper than when it is a sibling of a bare `$ref`. OpenAPI 3.1
/// allows `$ref` siblings, but keep this shape for the generated reference UI.
fn wrap_ref_siblings_for_redoc(v: &mut Value) {
    match v {
        Value::Object(map) => {
            for child in map.values_mut() {
                wrap_ref_siblings_for_redoc(child);
            }

            if let Some(ref_value) = map.remove("$ref") {
                if map.is_empty() {
                    map.insert("$ref".to_string(), ref_value);
                } else {
                    let mut ref_map = Map::new();
                    ref_map.insert("$ref".to_string(), ref_value);
                    map.insert(
                        "allOf".to_string(),
                        Value::Array(vec![Value::Object(ref_map)]),
                    );
                }
            }
        }
        Value::Array(items) => {
            for item in items {
                wrap_ref_siblings_for_redoc(item);
            }
        }
        _ => {}
    }
}

/// Schemars represents `Option<T>` as `anyOf: [T, null]`. That is accurate
/// JSON Schema, but ReDoc renders it as a two-tab choice for optional fields.
/// Collapse that generated nullable shape in the served OpenAPI so optionality
/// is expressed by the field being absent from `required`, matching the
/// hand-written API7/AISIX Cloud Admin API style.
fn collapse_nullable_any_of(v: &mut Value) {
    match v {
        Value::Object(map) => {
            for child in map.values_mut() {
                collapse_nullable_any_of(child);
            }

            let replacement = match map.get("anyOf") {
                Some(Value::Array(variants)) if variants.len() == 2 => {
                    let mut non_null = None;
                    let mut null_count = 0;
                    for variant in variants {
                        if variant.get("type") == Some(&Value::String("null".to_string())) {
                            null_count += 1;
                        } else {
                            non_null = Some(variant.clone());
                        }
                    }
                    if null_count == 1 {
                        non_null
                    } else {
                        None
                    }
                }
                _ => None,
            };

            if let Some(Value::Object(non_null)) = replacement {
                map.remove("anyOf");
                for (key, value) in non_null {
                    map.entry(key).or_insert(value);
                }
            }
        }
        Value::Array(items) => {
            for item in items {
                collapse_nullable_any_of(item);
            }
        }
        _ => {}
    }
}

/// Schemars can emit a pure `allOf: [{"$ref": "..."}]` wrapper around a
/// referenced schema. Collapse only the redundant pure wrapper. When the
/// property also carries a local description, default, or example, keep the
/// `allOf` form because ReDoc renders those sibling fields more reliably there
/// than next to a bare `$ref`.
fn collapse_single_ref_all_of(v: &mut Value) {
    match v {
        Value::Object(map) => {
            for child in map.values_mut() {
                collapse_single_ref_all_of(child);
            }

            let ref_value = match map.get("allOf") {
                Some(Value::Array(variants)) if variants.len() == 1 => {
                    variants[0].get("$ref").cloned()
                }
                _ => None,
            };

            if let Some(ref_value) = ref_value.filter(|_| map.len() == 1) {
                map.remove("allOf");
                map.entry("$ref".to_string()).or_insert(ref_value);
            }
        }
        Value::Array(items) => {
            for item in items {
                collapse_single_ref_all_of(item);
            }
        }
        _ => {}
    }
}

/// Walk a JSON value, rewriting `{"$ref": "#/definitions/X"}` strings
/// to `{"$ref": "#/components/schemas/X"}`. `schemars` 0.8 emits the
/// JSON Schema draft-07 form. OpenAPI 3.1 requires the
/// `components/schemas` form for in-document references.
fn rewrite_definitions_refs(v: &mut Value) {
    match v {
        Value::Object(map) => {
            for (k, val) in map.iter_mut() {
                if k == "$ref" {
                    if let Value::String(s) = val {
                        if let Some(suffix) = s.strip_prefix("#/definitions/") {
                            *val = Value::String(format!("#/components/schemas/{suffix}"));
                        }
                    }
                } else {
                    rewrite_definitions_refs(val);
                }
            }
        }
        Value::Array(items) => {
            for item in items {
                rewrite_definitions_refs(item);
            }
        }
        _ => {}
    }
}

