/// Build the merged OpenAPI document on first call and cache for the
/// process lifetime. The result is what `GET /admin/openapi.json`
/// serves. The constants above hold only the input fragments.
///
/// `pub(crate)` so [`crate::build_router`] can pre-warm it at startup
/// — `OnceLock::get_or_init` panics here would otherwise surface only
/// on the first `/admin/openapi.json` request, well after boot, which
/// is a worse ops failure mode than crashing immediately.
pub(crate) fn merged_openapi() -> &'static str {
    static CELL: OnceLock<String> = OnceLock::new();
    CELL.get_or_init(|| {
        let mut doc: Value =
            serde_json::from_str(OPENAPI_JSON_BASE).expect("OPENAPI_JSON_BASE must parse as JSON");

        for (name, raw) in RESOURCE_SCHEMAS {
            let mut schema: Value = serde_json::from_str(raw)
                .unwrap_or_else(|e| panic!("schema {name} must parse: {e}"));

            // Hoist `definitions/*` to top-level `components.schemas/*`.
            // The first definition wins — `Routing` appears both as a
            // standalone resource and nested in `Model`; they are
            // produced from the same Rust type so the content is
            // identical, but we still guard against double-write.
            if let Some(obj) = schema.as_object_mut() {
                if let Some(Value::Object(defs)) = obj.remove("definitions") {
                    for (def_name, def_value) in defs {
                        let target = &mut doc["components"]["schemas"][&def_name];
                        if target.is_null() {
                            *target = def_value;
                        }
                    }
                }
            }

            // Strip JSON Schema meta-fields that do not belong on an
            // inline OpenAPI 3.1 component schema.
            if let Some(obj) = schema.as_object_mut() {
                obj.remove("$schema");
                obj.remove("title");
            }

            doc["components"]["schemas"][*name] = schema;
        }

        // Rewrite `$ref: #/definitions/X` → `$ref: #/components/schemas/X`
        // everywhere in the merged doc (covers both the top-level
        // resource schemas and the already-hoisted nested ones).
        rewrite_definitions_refs(&mut doc);
        collapse_nullable_any_of(&mut doc);
        collapse_single_ref_all_of(&mut doc);
        add_variant_titles(&mut doc);
        add_missing_property_descriptions(&mut doc);
        add_schema_defaults(&mut doc);
        wrap_ref_siblings_for_redoc(&mut doc);

        serde_json::to_string(&doc).expect("merged OpenAPI must serialise")
    })
}

